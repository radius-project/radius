// Render renders the prompt for user.
//
// <AiDoc: The following comments were generated by OpenAI explaining the code as it existed at the time of this commit>
//	This function is used to render a list item in a list. It takes in a writer, a model, an index, and a list item as 
//	parameters. It checks if the list item is of the correct type, and if it is, it formats the item as a string with the 
//	index number and the item itself. It then uses a style to render the item, and if the index is equal to the model's 
//	index, it uses a different style to render the item. Finally, it prints the formatted item to the writer.
//
// </AiDoc: End of OpenAI comments>
func (d itemHandler) Render(w io.Writer, m list.Model, index int, listItem list.Item) {
	i, ok := listItem.(item)
	if !ok {
		return
	}

	str := fmt.Sprintf("%d. %s", index+1, i)

	fn := itemStyle.Render
	if index == m.Index() {
		fn = func(s string) string {
			return selectedItemStyle.Render("> " + s)
		}
	}

	fmt.Fprint(w, fn(str))
}

// NewListModel returns a list model for bubble tea prompt.
//
// <AiDoc: The following comments were generated by OpenAI explaining the code as it existed at the time of this commit>
//	This function creates a new ListModel object with a list of choices and a prompt message. It creates a list of items 
//	from the given choices, sets the title of the list to the prompt message, enables filtering, and sets the style of the 
//	title.
//
// </AiDoc: End of OpenAI comments>
func NewListModel(choices []string, promptMsg string) ListModel {
	items := make([]list.Item, len(choices))
	for i, choice := range choices {
		items[i] = item(choice)
	}

	l := list.New(items, itemHandler{}, defaultWidth, listHeight)
	l.Title = promptMsg
	l.SetShowStatusBar(false)
	l.SetFilteringEnabled(true)
	l.Styles.Title = titleStyle

	return ListModel{
		List: l,
	}
}

// ListMode represents the bubble tea model to use for user input
type ListModel struct {
	List     list.Model
	Choice   string
	Quitting bool
}

