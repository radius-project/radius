func (m ListModel) Init() tea.Cmd {
	return nil
}

// Update handles the updates from user input.
//
// <AiDoc: The following comments were generated by OpenAI explaining the code as it existed at the time of this commit>
//	This function is updating the ListModel based on the message that is passed in. Depending on the type of message, it 
//	will either quit the program, or select an item from the list.
//
// </AiDoc: End of OpenAI comments>
func (m ListModel) Update(msg tea.Msg) (tea.Model, tea.Cmd) {
	switch msg := msg.(type) {

	case tea.KeyMsg:
		switch keypress := msg.String(); keypress {
		case "ctrl+c", "esc", "q":
			m.Quitting = true
			return m, tea.Quit
		case "enter":
			if m.List.FilterState() != list.Filtering {
				i, ok := m.List.SelectedItem().(item)
				if ok {
					m.Choice = string(i)
				}
				return m, tea.Quit
			}
		}
	}

	var cmd tea.Cmd
	m.List, cmd = m.List.Update(msg)
	return m, cmd
}

// View renders the view after user selection.
//
// <AiDoc: The following comments were generated by OpenAI explaining the code as it existed at the time of this commit>
//	This function is used to display a list of choices to the user. It takes in a ListModel object which contains a List 
//	object and a Choice string. If the Choice string is not empty, it will render the title of the list and the Choice 
//	string. Otherwise, it will render the list of choices.
//
// </AiDoc: End of OpenAI comments>
func (m ListModel) View() string {
	if m.Choice != "" {
		return quitTextStyle.Render(fmt.Sprintf("%s: %s", m.List.Title, m.Choice))
	}

	return "\n" + m.List.View()
}
